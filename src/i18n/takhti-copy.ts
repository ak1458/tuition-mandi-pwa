function resolveLanguage(value?: string): LanguageCode {
  if (value === 'hi-roman') return 'hi-roman'
  if (value === 'hi') return 'hi'
  if (value?.startsWith('hi-roman')) return 'hi-roman'
  if (value?.startsWith('hi')) return 'hi'
  return 'en'
}

